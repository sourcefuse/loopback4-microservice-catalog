import {model, property} from '@loopback/repository';
import {CoreModel} from '@sourceloop/core';

@model()
export class SearchResult extends CoreModel<SearchResult> {
  @property({
    type: 'string',
    required: true,
  })
  name: string;

  @property({
    type: 'string',
    required: true,
  })
  description: string;
}

export type SearchResultWithRelations = SearchResult;
